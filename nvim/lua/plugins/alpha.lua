-- 自定义时间(在图片的下方)
local datetime = os.date("󱑂 %Y-%m-%d  %H:%M %A")
local function get_greeting()
    local hour = tonumber(os.date("%H"))
    if hour < 12 then
        return "󰖔 Good Morning"
    elseif hour < 18 then
        return "󰖙 Good Afternoon"
    else
        return "󰖔 Good Evening"
    end
end
local greeting = get_greeting() .. "  |  " .. datetime
