    type = "text",
    val = greeting,
    opts = {
        hl = "Special",
        position = "center",
    },
}

-- 自定义短语作为footer
local quotes = {
    { "Your time is limited, so don't waste it living someone else's life.", "Steve Jobs" },
    { "Stay hungry, stay foolish.", "Steve Jobs" },
    { "Move fast and break things.", "Mark Zuckerberg" },
    { "Talk is cheap. Show me the code.", "Linus Torvalds" },
    { "The only way to do great work is to love what you do.", "Steve Jobs" },
    { "First, solve the problem. Then, write the code.", "John Johnson" },
}

local function get_formatted_quote()
    math.randomseed(os.time())
    local pair = quotes[math.random(#quotes)]
    local quote_text = pair[1]
    local author = "— " .. pair[2]
    local quote_len = #quote_text
    local indent_size = quote_len - #author
    if indent_size < 0 then
        indent_size = 0
    end
    local indent = string.rep(" ", indent_size)
    return {
        quote_text,
        indent .. author,
    }
end
dashboard.section.footer.val = get_formatted_quote()
